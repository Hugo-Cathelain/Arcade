#include "arcade/core/API.hpp"

///////////////////////////////////////////////////////////////////////////////
// Namespace Arc
///////////////////////////////////////////////////////////////////////////////
namespace Arc
{

///////////////////////////////////////////////////////////////////////////////
std::queue<API::Event> API::mEvents;

///////////////////////////////////////////////////////////////////////////////
std::optional<API::Event> API::PollEvent(void)
{
    if (mEvents.empty()) {
        return (std::nullopt);
    }
    auto event = mEvents.front();
    mEvents.pop();
    return (event);
}

///////////////////////////////////////////////////////////////////////////////
void API::PushEvent(const Event& event)
{
    mEvents.push(event);
}

} // namespace Arc
