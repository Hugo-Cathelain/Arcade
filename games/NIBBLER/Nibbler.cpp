///////////////////////////////////////////////////////////////////////////////
void Nibbler::Tick(float deltaSeconds)
{
    (void)deltaSeconds;
    while (std::optional event = API::PollEvent()) {
        if (event->Is<API::Event::KeyPressed>()) {
            std::cout << "Key Pressed" << std::endl;
        }
    }
}

