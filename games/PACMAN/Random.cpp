namespace Arc::Pacman
{

///////////////////////////////////////////////////////////////////////////////
unsigned int RNG::mSeed = 0;

///////////////////////////////////////////////////////////////////////////////
unsigned int RNG::Get(void)
{
    mSeed = ((mSeed * 5 + 1) % 8192);
    return (static_cast<int>(ROM_6E[mSeed]));
}

///////////////////////////////////////////////////////////////////////////////
void RNG::SetSeed(unsigned int seed)
{
    mSeed = seed % 8192;
}

} // namespace Arc::Pacman
